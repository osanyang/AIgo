# 10000개의 멘티 데이터를 생성하는 함수
def generate_large_dummy_mentees(num_mentees):
    skills_levels = ['초급', '중급', '고급']

    mentee_data = []

    for i in range(num_mentees):
        mentee = {
            '멘티_ID': i+1,
            '현재 기술 수준': random.choice(skills_levels),
            '학습 목표': random.choice(extended_learning_goals),
            '관심 분야': random.choice(extended_interests)
        }
        mentee_data.append(mentee)

    return pd.DataFrame(mentee_data)

# 10000명의 멘티 데이터를 생성
large_mentees_df = generate_large_dummy_mentees(10000)

# CSV 파일로 저장
large_mentees_df.to_csv('mentees_10000.csv', index=False)

